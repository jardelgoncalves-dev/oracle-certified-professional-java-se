
public class SwitchCase {

	public static void main(String[] args) {
		char letter = 'A';
		
		switch(letter) {
			case 'C':
				System.out.println("letter is C");
				break;
			case 'D':
				System.out.println("letter is D");
				break;
			case 'A':
				System.out.println("letter is A");
				break;
			default:
				System.out.println("letter is not found");
		}

	}

}
