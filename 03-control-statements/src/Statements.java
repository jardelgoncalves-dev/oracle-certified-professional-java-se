
public class Statements {

	public static void main(String[] args) {
		
		int a = 3, b = 2;
		// IF/ELSE
		System.out.println("a is " + a);
		System.out.println("b is " + b);
		if (a> b) {
			a--;
		} else if (a < b) {
			a++;
		} else {
			b++;
		}
		System.out.println("after if/else");
		System.out.println("a is " + a);
		System.out.println("b is " + b);
		
		

	}

}
